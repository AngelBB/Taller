    private void btn_consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_consultarActionPerformed
        // TODO add your handling code here:
        Controlador_mecanico ca=new Controlador_mecanico();

        ArrayList<ArrayList<String>>listaClientes=ca.consultaMecanico(con_dni.getSelectedItem().toString());
        DefaultTableModel modelo;
        modelo=(DefaultTableModel) tabla_consultas.getModel();
        for(int i=0;i<listaClientes.size();i++){
            modelo.insertRow(i, listaClientes.get(i).toArray());
        }
        tabla_consultas.setModel(modelo);
    }//GEN-LAST:event_btn_consultarActionPerformed

