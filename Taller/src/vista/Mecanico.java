    private void act_mec_selectItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_act_mec_selectItemStateChanged
        // TODO add your handling code here:
        Controlador_mecanico ca=new  Controlador_mecanico();
       if(evt.getStateChange()==ItemEvent.SELECTED){
        ca.recogerDatosMecanico(act_mec_select.getSelectedItem().toString());
        act_dni.setText(ca.getDni());
        act_nombre.setText(ca.getNombre());
        act_ape1.setText(ca.getApellido1());
        act_ape2.setText(ca.getApellido2());
        act_dir.setText(ca.getDireccion());
        act_tlf.setText(Integer.toString(ca.getTlf()));
        act_sal.setText(Float.toString(ca.getSalario()));
        act_contra.setText(Integer.toString(ca.getTlf()));
                
       }
    }//GEN-LAST:event_act_mec_selectItemStateChanged

