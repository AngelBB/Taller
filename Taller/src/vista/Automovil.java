    private void btn_consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_consultarActionPerformed
        // TODO add your handling code here:
        Controlador_automovil ca=new Controlador_automovil();

        ArrayList<ArrayList<String>>listaAutos=ca.consultaVehiculos(veh_consulta.getSelectedItem().toString());
        DefaultTableModel modelo;
        modelo=(DefaultTableModel) veh_tabla_consultas.getModel();
        for(int i=0;i<listaAutos.size();i++){
            modelo.insertRow(i, listaAutos.get(i).toArray());
        }
        veh_tabla_consultas.setModel(modelo);

    }//GEN-LAST:event_btn_consultarActionPerformed

