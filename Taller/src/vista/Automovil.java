    private void veh_cli_select_actItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_veh_cli_select_actItemStateChanged
        //Creamos la instancia del controlador
        Controlador_automovil ca=new Controlador_automovil();

        //Consultamos los datos
        ArrayList<String> autos=ca.cargarAutos(veh_cli_select_act.getSelectedItem().toString());//Le pasamos el dni del cliente
        
        veh_mat_select_act.removeAllItems();
        //Cargamos los datos devueltos a los comboboxes
        for (int i=0;i<autos.size();i++){
            veh_mat_select_act.addItem(autos.get(i));
        }
        // veh_mat_select_act.repaint();//repintamos
    }//GEN-LAST:event_veh_cli_select_actItemStateChanged

