    private void veh_mat_select_actItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_veh_mat_select_actItemStateChanged
        
        if(evt.getStateChange()==ItemEvent.SELECTED){  

            Controlador_automovil ca=new Controlador_automovil();
            ca.recogerDatosVehiculo(veh_cli_select_act.getSelectedItem().toString(), veh_mat_select_act.getSelectedItem().toString());

            marca_act.setText(ca.getMarca());
            modelo_act.setText(ca.getModelo());
            matricula_act.setText(ca.getMatricula());
            dni_act.setSelectedItem(ca.getDni_propietario());
            color_act.setText(ca.getColor());
            cod_conesionario_act.setSelectedItem(ca.getCod_concesionario());

        }
           
    }//GEN-LAST:event_veh_mat_select_actItemStateChanged

